public class OgrencinNumarasi {
    public static void main(String[] args) {
        int ogrenci_numarasi=1;

        switch (ogrenci_numarasi) {
            case 1:
                System.out.println("Öğrenci numarası:1 Öğrenci adı:Arven");
                break;
            case 2:
                System.out.println("Öğrenci numarası:2 Öğrenci adı:Serdar");
                break;
            case 3:
                System.out.println("Öğrenci numarası:3 Öğrenci adı:Bengi");
                break;
            case 4:
                System.out.println("Öğrenci numarası:4 Öğrenci adı:Sevgi");
                break;
            default:
                System.out.println("böyle bir öğrenci numarası bulunmamaktadır.");

        }
    }
}
